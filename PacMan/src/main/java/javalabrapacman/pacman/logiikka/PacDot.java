package javalabrapacman.pacman.logiikka;

/**
 *
 * @author Letixari
 */
public class PacDot {

    private int X;
    private int Y;

    public PacDot(int x, int y) {
        this.X = x;
        this.Y = y;
    }

    public int getX() {
        return this.X;
    }

    public int getY() {
        return this.Y;
    }
}
